from __future__ import annotations


def _help(
    purpose: str,
    default: str,
    options: str,
    when: str,
    risk: str,
    visibility: str = "",
) -> str:
    parts = [
        f"用途：{purpose}",
        f"默认建议：{default}",
        f"选项说明：{options}",
        f"何时修改：{when}",
        f"设置不当的风险：{risk}",
    ]
    if visibility:
        parts.append(f"界面策略：{visibility}")
    return "\n\n".join(parts)


